package exCollection2;



public class Aula {
	
// Variaveis do tipo String e inteiro.
private String titulo;
private int tempo;


// Método Construtor
public Aula(String titulo, int tempo) {
this.titulo = titulo;
this.tempo = tempo;
}
 

// Métodos Getters 
public String getTitulo() {
return titulo;
}

public int getTempo() {
return tempo;
}
}



 
